import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";

import { buildSpecQuality, repoRoot } from "./spec-quality-lib.mjs";

const generatedDir = path.join(repoRoot, "src", "generated");
const outputPath = path.join(generatedDir, "specQuality.ts");
async function main() {
  const qualityByTitle = await buildSpecQuality();
  await mkdir(generatedDir, { recursive: true });
  const output = `// This file is auto-generated by tools/generate-spec-quality.mjs.\n` +
    `// Do not edit by hand.\n\n` +
    `export const generatedSpecDataByTitle = ${JSON.stringify(qualityByTitle, null, 2)} as const;\n`;
  await writeFile(outputPath, output, "utf8");
  console.log(`Generated ${path.relative(repoRoot, outputPath)}`);
}

await main();
